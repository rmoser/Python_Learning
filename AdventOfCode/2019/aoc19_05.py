def intcode(input_value, arr):
    i = 0
    outputs = []
    while True:
        inst = str(arr[i]).rjust(5, '0')
        op = int(inst[-2:])  # opcode
        c_mode, b_mode, a_mode = (int(x) for x in inst[:3])

        # print('i:', i, inst, arr[i:i+5], arr[i], op)


        match op:
            case 1:
                a, b, c = arr[i+1:i+4]
                # print(f'\tA {a_mode}: {a}')
                # print(f'\tB {b_mode}: {b}')
                # print(f'\tC {c_mode}: {c}')

                a = a if a_mode else arr[a]
                b = b if b_mode else arr[b]
                # print(f'op {op}: Add {a} + {b} => {c}')
                arr[c] = a + b
                i += 4

            case 2:
                a, b, c = arr[i+1:i+4]
                # print(f'\tA {a_mode}: {a}')
                # print(f'\tB {b_mode}: {b}')
                # print(f'\tC {c_mode}: {c}')

                a = a if a_mode else arr[a]
                b = b if b_mode else arr[b]
                # print(f'op {op}: Mul {a} * {b} => {c}')
                arr[c] = a * b
                i += 4

            case 3:
                a = arr[i+1]
                # print(f'\tA {a_mode}: {a}')

                if a_mode:
                    raise ReferenceError(f"Invalid a_mode: {a_mode} in {inst}")
                # TODO: Store Input to arr[a]
                # print(f'op {op}: Stor {input_value} => {a}')
                arr[a] = input_value
                i += 2
                pass

            case 4:
                # TODO: Output from arr[a]
                a = arr[i+1]
                # print(f'\tA {a_mode}: {a}')

                # print(f'op {op}: Output {a}')
                a = a if a_mode else arr[a]
                outputs.append(a)
                i += 2
                pass

            case 5:  # Jmp If True
                a, b = arr[i+1:i+3]
                # print(f'\tA {a_mode}: {a}')
                # print(f'\tB {b_mode}: {b}')

                a = a if a_mode else arr[a]
                b = b if b_mode else arr[b]
                # print(f'op {op}: JMP True: {a} => {b}')
                if a != 0:
                    i = b
                else:
                    i += 3
                pass

            case 6:  # Jmp if False
                a, b = arr[i+1:i+3]
                # print(f'\tA {a_mode}: {a}')
                # print(f'\tB {b_mode}: {b}')

                a = a if a_mode else arr[a]
                b = b if b_mode else arr[b]
                # print(f'op {op}: JMP False: {a} => {b}')
                if a == 0:
                    i = b
                else:
                    i += 3
                pass

            case 7:  # Less Than
                a, b, c = arr[i+1:i+4]
                # print(f'\tA {a_mode}: {a}')
                # print(f'\tB {b_mode}: {b}')
                # print(f'\tC {c_mode}: {c}')

                a = a if a_mode else arr[a]
                b = b if b_mode else arr[b]
                # print(f'op {op}: LT: {a} < {b}')
                arr[c] = int(a < b)
                i += 4
                pass

            case 8:  # Equals
                a, b, c = arr[i+1:i+4]
                # print(f'\tA {a_mode}: {a}')
                # print(f'\tB {b_mode}: {b}')
                # print(f'\tC {c_mode}: {c}')

                a = a if a_mode else arr[a]
                b = b if b_mode else arr[b]
                # print(f'op {op}: EQ: {a} == {b}')
                arr[c] = int(a == b)
                i += 4
                pass

            case 99:
                break

            case _:
                raise IndexError(f"Invalid opcode {op}")

    return outputs
    # print(arr)
    return arr[0]


